public class Trip {

    String name;
    String location;
    String date;

    public String getName() {
        return name;
    }

    public void setName(String commentPersonName) {
        this.name = commentPersonName;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String comment) {
        this.location = comment;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String comment) {
        this.date = comment;
    }

}
